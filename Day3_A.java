public class Day3_A {
    public static void main(String[] args) {
        byte a=5, b=10;

        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a!=b);

        System.out.println((a>b) && (a<b));
        System.out.println((a>b) || (a<b));    
        System.out.println(!(a>b));
    }
}
